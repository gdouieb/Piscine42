#include "prototypes.h"

int	ft_check_input(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
		{
			ft_print_error();
			return (0);
		}
		i++;
	}
	return (i);
}


